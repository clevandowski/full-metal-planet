function initPlateau(PLATEAU) {
	var plateau = [];

	for (j = 0; j < PLATEAU.length; j++) {
		var line = PLATEAU[j];
		plateau[j] = [];
		for (i = 0; i < line.length; i++) {
			var caseType = line[i];
			plateau[j][i] = new FMPCase(caseType, i, j);
		}
	}

	return plateau;
}
